int	ft_sqrt(int nb)
{
	int	i;
	int	s;

	i = 1;
	s = 0;
	while (i < nb && s != i && i < 46340)
	{
		i++;
		s = (nb / i);
	}
	if (i * i == nb)
		return (i);
	else
		return (0);
}
